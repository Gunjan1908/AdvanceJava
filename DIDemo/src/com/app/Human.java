package com.app;
public class Human {
	Heart heart;
 
	public Human() {
		System.out.println("Human Constructor");
	}

	public Human(Heart heart) {
		super();
		System.out.println("In Constructor---->");
		this.heart = heart;
	}

	public void setHeart(Heart heart) {
		System.out.println("In Setter----->");
		this.heart = heart;
	}
	
	public void pumping()
	{
		if(heart!=null)
		{
		heart.pump();
		}
		else
		{
			System.out.println("You are Dead!!!");
		}
	}

}
